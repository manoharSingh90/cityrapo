// GOOGLE MAP 
var geocoder = new google.maps.Geocoder();

function geocodePosition(pos) {
    geocoder.geocode({
        latLng: pos
    }, function(responses) {
        if (responses && responses.length > 0) {
            //updateMarkerAddress(responses[0].formatted_address.replace(", Deutschland", "").split(", ").splice(-1, 1));
            updateMarkerAddress(responses[0].formatted_address);
        } else {
            updateMarkerAddress('Cannot determine address at this location.');
        }
    });
}

function updateMarkerPosition(latLng) {
    document.getElementById('coordinatesval').value = [
        latLng.lat(),
        latLng.lng()
    ].join(', ');
}

function updateMarkerAddress(str) {
    document.getElementById('searchTextField').value = str;
}

function initialize() {
  
    var latLng = new google.maps.LatLng(28.612903658948674, 77.23139198046874);
    var map = new google.maps.Map(document.getElementById('googleMap'), {
        zoom: 12,
        center: latLng,
        disableDefaultUI: true,
        mapTypeId: google.maps.MapTypeId.ROADMAP
    });
        
    var marker = new google.maps.Marker({
        position: latLng,
        map: map,
        draggable: true,
        title: "You want a maker at this point"
    });

    var input = document.getElementById('searchTextField');
    var autocomplete = new google.maps.places.Autocomplete(input,{
    componentRestrictions: {
          country: 'in'
        }
  });

    autocomplete.bindTo('bounds', map);

    // Update current position info.
    updateMarkerPosition(latLng);

    // Add dragging event listeners.
    google.maps.event.addListener(marker, 'dragstart', function() {
        updateMarkerAddress('Dragging...');
    });

    google.maps.event.addListener(marker, 'drag', function() {
        updateMarkerPosition(marker.getPosition());
    });

    google.maps.event.addListener(marker, 'dragend', function() {
        map.setZoom(14);
        map.setCenter(marker.getPosition());
        geocodePosition(marker.getPosition());
    });

    google.maps.event.addListener(autocomplete, 'place_changed', function() {
        input.className = '';
        var place = autocomplete.getPlace();
        if (!place.geometry) {
            // Inform the user that the place was not found and return.
            input.className = 'notfound';
            return;
        }

        // If the place has a geometry, then present it on a map.
        if (place.geometry.viewport) {
            map.fitBounds(place.geometry.viewport);
        } else {
            map.setCenter(place.geometry.location);
            map.setZoom(12); 
        }
        marker.setPosition(place.geometry.location);
        updateMarkerPosition(marker.getPosition());

    });
}

// Onload handler to fire off the app.
google.maps.event.addDomListener(window, 'load', initialize);
